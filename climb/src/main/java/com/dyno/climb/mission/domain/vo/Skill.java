package com.dyno.climb.mission.domain.vo;

public enum Skill {
  DYNO, // Dynamic movement to reach a distant hold
  HIGH_STEP, // Stepping high onto a foothold
  MANTLE, // Pushing down to transition over a ledge
  SMEAR, // Using friction on the wall instead of footholds
  TOE_HOOK, // Hooking the toe over a hold for leverage
  HEEL_HOOK, // Placing the heel on a hold to create tension
  CROSS_THROUGH, // Reaching across the body to a hold
  FLAGGING, // Extending a leg for balance
  DROP_KNEE, // Dropping a knee for better positioning
  SIDE_PULL, // Grabbing a sideways oriented hold
  UNDERCLING, // Grabbing a hold from underneath
  LUNGE // Stretching and lunging for a distant hold
}
